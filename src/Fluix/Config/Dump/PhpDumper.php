<?php

declare(strict_types = 1);

namespace Fluix\Config\Dump;

use Fluix\Config\Dumper;
use Fluix\Config\File;

final class PhpDumper implements Dumper
{
    public function dump(File $file, array $config): void
    {
        ob_start();
        echo "<" . "?php\n";
        echo "// generated by " . static::class . ", do not edit\n";
        echo "return " . var_export($config, true) . ";";
        $file->write(ob_get_clean());
    }
}
